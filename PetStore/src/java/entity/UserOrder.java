package entity;

/**
 *
 * @author vuhai
 */
public class UserOrder {

    private int id;
    private String userId;

    public UserOrder() {
    }

    public UserOrder(int id, String userId) {
        this.id = id;
        this.userId = userId;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }


}
